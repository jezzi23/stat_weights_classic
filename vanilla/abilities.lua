local function level_scaling(lvl)
    return math.min(1, 1 - (20 - lvl)* 0.0375);
end

local function spell_coef(spell_info, k)

    local direct_coef = math.min(1.0, math.max(1.5/3.5, spell_info.cast_time/3.5));
    local ot_coef = math.min(1.0, spell_info.over_time_duration/15.0);

    if  bit.band(spell_info.flags, spell_flags.periodic_no_coef) ~= 0  then
        ot_coef = 0;

    elseif spell_info.cast_time == spell_info.over_time_duration then
        ot_coef = direct_coef;
        direct_coef = 0;
    elseif spell_info.base_min > 0 and spell_info.over_time > 0 then
        local direct_coef_p = direct_coef*direct_coef / (direct_coef + ot_coef);
        local ot_coef_p = ot_coef*ot_coef / (direct_coef + ot_coef);

        direct_coef = direct_coef_p
        ot_coef = ot_coef_p;
    end

    if spell_info.base_min == 0 then
        direct_coef = 0;
    end
    if spell_info.over_time == 0 then
        ot_coef = 0;
    else
        ot_coef = ot_coef * spell_info.over_time_tick_freq/spell_info.over_time_duration;
    end


    if bit.band(spell_info.flags, spell_flags.aoe) ~= 0 then
        direct_coef = direct_coef/3;
        ot_coef = ot_coef/3;
    end
    if bit.band(spell_info.flags, spell_flags.snare) ~= 0 then
        direct_coef = direct_coef * 0.95;
        ot_coef = ot_coef * 0.95;
    end

    if bit.band(spell_info.flags, spell_flags.absorb) ~= 0 then
        direct_coef = 0.1;
    end
    if bit.band(spell_info.flags, spell_flags.hybrid) ~= 0 then
        direct_coef = direct_coef * 0.5;
        ot_coef = ot_coef * 0.5;
    end

    return direct_coef, ot_coef;
end

